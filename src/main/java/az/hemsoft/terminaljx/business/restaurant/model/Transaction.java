package az.hemsoft.terminaljx.business.restaurant.model;

import java.util.Date;
import java.util.List;

public class Transaction {
    private String transactionId;
    private TransactionType type; // SALE, PURCHASE
    private Date date;
    private String entityId; // Customer ID or Supplier ID
    private double totalAmount;
    private double discount;
    private double finalAmount;
    private String companyMail;

    // Not persisted directly in transaction table, but useful for logic
    private List<TransactionDetail> details;

    public enum TransactionType {
        SALE,
        PURCHASE,
        RETURN_SALE,
        RETURN_PURCHASE
    }

    public Transaction() {
    }

    public String getTransactionId() {
        return transactionId;
    }

    public void setTransactionId(String transactionId) {
        this.transactionId = transactionId;
    }

    public TransactionType getType() {
        return type;
    }

    public void setType(TransactionType type) {
        this.type = type;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getEntityId() {
        return entityId;
    }

    public void setEntityId(String entityId) {
        this.entityId = entityId;
    }

    public double getTotalAmount() {
        return totalAmount;
    }

    public void setTotalAmount(double totalAmount) {
        this.totalAmount = totalAmount;
    }

    public double getDiscount() {
        return discount;
    }

    public void setDiscount(double discount) {
        this.discount = discount;
    }

    public double getFinalAmount() {
        return finalAmount;
    }

    public void setFinalAmount(double finalAmount) {
        this.finalAmount = finalAmount;
    }

    public String getCompanyMail() {
        return companyMail;
    }

    public void setCompanyMail(String companyMail) {
        this.companyMail = companyMail;
    }

    public List<TransactionDetail> getDetails() {
        return details;
    }

    public void setDetails(List<TransactionDetail> details) {
        this.details = details;
    }
}

